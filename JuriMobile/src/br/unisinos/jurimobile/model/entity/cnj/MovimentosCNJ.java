package br.unisinos.jurimobile.model.entity.cnj;

public class MovimentosCNJ {

	private Long codigoMovimento;

	private String movimento;

	private String glossario;

	public Long getCodigoMovimento() {
		return codigoMovimento;
	}

	public void setCodigoMovimento(Long codigoMovimento) {
		this.codigoMovimento = codigoMovimento;
	}

	public String getMovimento() {
		return movimento;
	}

	public void setMovimento(String movimento) {
		this.movimento = movimento;
	}

	public String getGlossario() {
		return glossario;
	}

	public void setGlossario(String glossario) {
		this.glossario = glossario;
	}

}
